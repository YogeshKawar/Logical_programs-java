package elseIfLadder;

public class elseIfLadder4 {
public static void main(String[] args) {
	int number = -2;

    if (number > 0) {
      System.out.println("The number is positive.");
    }

    else if (number < 0) {
      System.out.println("The number is negative.");
    }
    
    else {
      System.out.println("The number is 0.");
}
}
}